    private void habilitarEdicao(){
        // Text
        txtSiglaCurso.setEnabled(false);
        
        fTxtValorHoraInstrutor.setEnabled(true);
        fTxtCargaHoraria.setEnabled(true);
        txtNomeCurso.setEnabled(true);
        txtProgramaCurso.setEnabled(true);
        fTxtValorCurso.setEnabled(true);
        fTxtDataVigencia.setEnabled(true);
        
        // Botões
        btnConsultar.setEnabled(false);
        
        btnAlterar.setEnabled(true);
        btnInserir.setEnabled(false);
        btnExcluir.setEnabled(true);
        
        // radiobutton
        radioButtonOnline.setEnabled(true);
        radioButtonPresencial.setEnabled(true);
        
        // Deixando o curso atual visível
        Curso curso = cadCursos.get(atualCurso);
        fTxtValorHoraInstrutor.setText(Double.toString(curso.getValorHoraInstrutor()));
        fTxtCargaHoraria.setText(Integer.toString(curso.getCargaHorario()));
        txtNomeCurso.setText(curso.getNome());
        txtProgramaCurso.setText(curso.getPrograma());
        fTxtValorCurso.setText(Double.toString(curso.getValor()));
        fTxtDataVigencia.setText(curso.getDataVigencia());
        
        if(curso.getTipo().equals("Presencial")){
            radioButtonPresencial.setSelected(true);
        }else{
            radioButtonOnline.setSelected(true);
        }
        
        // Focus
        txtNomeCurso.requestFocus();
    }
    
