    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        txtSiglaCurso.setText(txtSiglaCurso.getText().toUpperCase());
        
        // Caso não tenha nada escrito é retornado
        if(txtSiglaCurso.getText().length() < 1)
            return;
        
        atualCurso = -1;
        
        for (int index = 0; index < cadCursos.size(); index++){
            if(txtSiglaCurso.getText().equals(cadCursos.get(index).getSigla()))
                atualCurso = index;
        }
        
        if(atualCurso < 0){
            habilitarInsercao();
            return;
        }
        
        habilitarEdicao();
    }//GEN-LAST:event_btnConsultarActionPerformed

    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
        
        Curso curso = new Curso(txtSiglaCurso.getText(), txtNomeCurso.getText());
        curso.setCargaHorario(Integer.parseInt(fTxtCargaHoraria.getText()));
        curso.setDataVigencia(fTxtDataVigencia.getText());
        curso.setPrograma(txtProgramaCurso.getText());
        curso.setValor(Double.parseDouble(fTxtValorCurso.getText().replace(",", ".")));
        curso.setValorHoraInstrutor(Double.parseDouble(fTxtValorHoraInstrutor.getText().replace(",", ".")));
        
        // Tipo
        if(radioButtonOnline.isSelected()){
            curso.setTipo("Presencial");
        }else{
            curso.setTipo("Online");
        }
        
        cadCursos.add(curso);
        
        habilitarConsulta();
    }//GEN-LAST:event_btnInserirActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        Curso curso = cadCursos.get(atualCurso);
        curso.setNome(txtNomeCurso.getText());
        curso.setCargaHorario(Integer.parseInt(fTxtCargaHoraria.getText()));
        curso.setDataVigencia(fTxtDataVigencia.getText());
        curso.setPrograma(txtProgramaCurso.getText());
        curso.setValor(Double.parseDouble(fTxtValorCurso.getText().replace(",", ".")));
        curso.setValorHoraInstrutor(Double.parseDouble(fTxtValorHoraInstrutor.getText().replace(",", ".")));
        
        // Tipo
        if(radioButtonPresencial.isSelected()){
            curso.setTipo("Presencial");
        }else{
            curso.setTipo("Online");
        }
        
        habilitarConsulta();
    }//GEN-LAST:event_btnAlterarActionPerformed

